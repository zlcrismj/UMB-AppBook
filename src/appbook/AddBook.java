    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        String nombre = txtname.getText();
        String carrera = jComboBox2.getSelectedItem().toString();
        String grupo = jComboBox1.getSelectedItem().toString();
        String specialized = txtespecializado.getText();
        String maestro = txtmaestro.getText();
        sql s = new sql();
        int codigo = s.auto_increment("SELECT MAX(codigo_pdf) FROM archivos;");
        File ruta = new File(ruta_archivo);
        if (nombre.trim().length() != 0 && carrera.trim().length() != 0 && grupo.trim().length() != 0 && specialized.trim().length() != 0 && maestro.trim().length() != 0 && ruta_archivo.trim().length() != 0) {
            guardar_pdf(codigo, nombre, carrera, grupo, specialized, maestro, ruta);
            tpdf.visualizar_PdfVO(tabla);
            ruta_archivo = "";
            activa_boton(false, false, false);
            txtname.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Fill in all fields");
        }
    }//GEN-LAST:event_btnguardarActionPerformed

