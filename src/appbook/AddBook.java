        public void guardar_pdf(int codigo, String nombre, String carrera, String grupo, String specialized, String maestro, File ruta) {
        MostrarPDF pa = new MostrarPDF();
        MostrarPdfVO po = new MostrarPdfVO();
        po.setCodigopdf(codigo);
        po.setNombrepdf(nombre);
        po.setCarrerapdf(carrera);
        po.setGrupopdf(grupo);
        po.setSpecializedpdf(specialized);
        po.setMaestropdf(maestro);
        try {
            byte[] pdf = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(pdf);
            po.setArchivopdf(pdf);
        } catch (IOException ex) {
            po.setArchivopdf(null);
            System.out.println("Error Adding PDF File"+ex.getMessage());
        }
        pa.Agregar_PdfVO(po);
    }

